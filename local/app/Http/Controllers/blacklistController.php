    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
      $bl=new blacklist;
      $user= \App\user_tb::where('id', $request->user_id)->first();
      $bl->username = $user->user_username;
      $bl->fullname = $user->user_fullname;
      $bl->account_bank = $user->user_account;
      $bl->bank = $user->user_bank_name;
      $bl->user_id = $request->user_id;
      $bl->save();

      Session::flash('success','Add blacklist complete');
      return redirect('backoffice/blacklist');
    }

